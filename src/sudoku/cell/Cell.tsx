import React from 'react'

import { Cell, Coordinate } from '../definitions'
import "./Cell.css"

const Candidates = () => {

}

interface Props extends Cell {
  setSelectedCell: (coordinate: Coordinate) => void
}

export default (props: Props): JSX.Element => {
  const { value, autogenerated, coordinate, solution, setSelectedCell } = props
  return (
    <div
      className="grid__item"
      onClick={() => !autogenerated && setSelectedCell(coordinate)}
    >
      <div className="content">
        <div className="content-inside">
          {autogenerated && <h2 className="autogenerated">{solution}</h2>}
          {value && (
            <h2 className={`${value === solution ? "completed" : "incorrect"}`}>
              {value}
            </h2>
          )}
        </div>
      </div>
    </div>
  );
};